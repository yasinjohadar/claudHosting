    /**
     * العلاقة مع المستخدم (إن وُجد — للمستخدمين المسجّلين من الموقع).
     */
    public function user()
    {
        return $this->belongsTo(User::class);
    }

    /**
     * العلاقة مع الفواتير
     */
    public function invoices()
    {
        return $this->hasMany(Invoice::class, 'whmcs_client_id', 'whmcs_id');
    }

    /**
     * العلاقة مع التذاكر
     */
    public function tickets()
    {
        return $this->hasMany(Ticket::class, 'whmcs_client_id', 'whmcs_id');
    }

    /**
     * العلاقة مع جهات الاتصال
     */
    public function contacts()
    {
        return $this->hasMany(Contact::class);
    }

    /**
     * العلاقة مع المنتجات/الخدمات
     */
    public function products()
    {
        return $this->belongsToMany(Product::class, 'customer_products', 'customer_id', 'product_id')
            ->withPivot(['id', 'whmcs_service_id', 'domain', 'username', 'domainstatus', 'nextduedate', 'amount', 'billingcycle', 'created_at', 'updated_at'])
            ->wherePivotNull('deleted_at');
    }

    /**
     * الحصول على الاسم الكامل
     */
    public function getFullNameAttribute()
    {
        return $this->fullname ?? trim($this->firstname . ' ' . $this->lastname);
    }

