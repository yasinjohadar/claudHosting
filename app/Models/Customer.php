    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'user_id',
        'whmcs_id',
        'firstname',
        'lastname',
        'fullname',
        'email',
        'companyname',
        'address1',
        'address2',
        'city',
        'state',
        'postcode',
        'country',
        'phonenumber',
        'currency',
        'groupid',
        'status',
